export default async function ServicePage({ params }) {
  const { slug } = await params;
  let service = null;
  try {
    const res = await fetch(`${process.env.NEXT_PUBLIC_BASE_URL}/createPage/pageByid/?url=${slug}`, {
      cache: "no-store",
      method: 'GET'
    });

    if (!res.ok) throw new Error(`Failed to fetch: ${res.status}`);
    const response = await res.json();
    if (response.status) {

      service = response.pagedetail;

    }

  } catch (error) {
    console.error("Fetch error:", error);
  }


  if (!service) {
    return <div>service not found</div>; // fallback if slug doesn't match
  }

  return <ServiceContent service={service} />;
}
